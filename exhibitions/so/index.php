<h2>Y, ¿entonces?</h2><br>


<p>
Re-opening of THE 9.99 gallery.<br>

Friday june 14th, 2013.
</p><br>

<p>La exposición <span style="font-style:italic">Y … ¿Entonces?</span> que inaugura el nuevo espacio de The 9.99 Gallery propone retomar el diálogo artístico que se interrumpió con la clausura de su anterior espacio expositivo.  De esta forma, se reanuda uno de los pocos proyectos en la zona que tratan de dar visibilidad al arte que se está produciendo actualmente en Guatemala. Esta primera exposición presenta trabajos entorno al objeto de ocho artistas establecidos y emergentes de la región.</p><br>


<p>En las últimas décadas el universo objetual ha pasado a ser de suma importancia para el entendimiento del arte actual. Para comprender los cambios que han derivado en este panorama es necesario volver la vista hacia la modernidad, al momento en el que la mímesis de lo natural dejó de representar el entorno real y el objeto comenzó a fraguarse como un símbolo de la vida moderna. Las ideas de Marcel Duchamp, gran precedente del arte conceptual,  nacen de una modernidad plagada de objetos industriales hechos en masa en los que lo artesanal desaparece para diluirse en un anonimato uniforme.  El arte objetual, sin embargo, devuelve con su gesto la relevancia al objeto como elemento artístico convirtiéndolo en único de la mano del artista y provocando al mismo tiempo cuestiones acerca de la autoría y la importancia del concepto o lo intelectual frente a aquello creado con las propias manos.</p><br>


<p>Los artistas participantes en esta exposición exploran el objeto desde diferentes puntos de vista, en algunos casos se trata de una investigación primordialmente formal sobre el objeto y su representación. Diana de Solares, por ejemplo, transforma materiales encontrados para crear estructuras abstractas minimalistas trasmutando los espacios expositivos en lugares íntimos. Tepeu Choc realiza también modificaciones, en su caso dentro del mundo de las artes gráficas, al utilizar letras de diferentes tipografías para crear redes abstractas.  Mientras, Darío Escobar prosigue su diálogo sobre las líneas barrocas en el espacio con obras como <span style="font-style:italic">Escultura transparencte No. 2</span> y al mismo tiempo da un paso más en su trasgresión de la autoría utilizando conjuntamente el azar como elemento artístico en obras como <span style="font-style:italic">Pintura abstracta No. 7</span>, realizadas en herrerías en donde el artista deja que el lienzo vaya formándose a lo largo del trabajo del herrero que utiliza la tela como fondo durante su proceso de trabajo.</p><br>


<p>Edgar Calel, Benvenuto Chavajay y Angel Poyón trabajan la escultura a partir del objeto. Provenientes del interior de país, más específicamente de San Pedro la Laguna y San Juan de Comalapa, sus obras aluden al mundo rural o la cultura popular a través de piezas conceptuales de objetos intervenidos.  La arcilla y el maguey de Chavajay explorando el vacío, los sombreros de Poyón con dibujos entorno a la geografía y el progreso o los trece hilos de alambre en alusión al ciclo maya de Calel son algunos ejemplos del tipo de obra de estos artistas capaces de combinar el pasado con el presente utilizando un lenguaje estético internacional.</p><br>


<p>Existe también una importante vertiente de la fotografía interesada en explorar el objeto. Andrea Aragón pertenece a ese grupo de artistas, en su caso la importancia de la representación del objeto no es el objeto en si sino las ausencias que éstos insinúan fomentando una especie de misterio y preguntas con cada imagen. El objeto también ha dado el salto al mundo del perfomance, Aníbal López no sólo utiliza objetos en sus acciones en piezas como <span style="font-style:italic">Gratis</span> en las que cuestiona el valor económico y artístico de simples cajas de cartón sino que además presenta la objetualización del individuo en obras como <span style="font-style:italic">Limpieza</span>.</p><br>


<p>Tal y como se manifiesta a lo largo de  Y … ¿Entonces?,  prácticamente un siglo después de la aparición de los primeros ready-mades, el objeto sigue siendo pieza fundamental del mundo del arte. A lo largo de los años el campo objetual  ha venido expandiéndose en múltiples direcciones y abarca hoy prácticamente todos los géneros provocando una amplia experimentación. En realidad el uso del objeto no es más que el reflejo de la vida actual en la que los bienes materiales y el consumo son la base de nuestra sociedad. Los artistas participantes en esta exposición a lo largo de sus prácticas artísticas no hacen más que atestiguar las características de nuestra vida moderna hoy en día. </p><br>


<p style="text-align:right;">Idurre Alonso</p><br>


